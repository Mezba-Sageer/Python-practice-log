#functions in set

st={10,32,45,56,86,97,45,1,23,95,101,10}

#to find sum
#for sum of a set we can use - sum()
#duplicate values in the set only gets added to the sum once since it doesn't support duplicates
print(sum(st))

#to find lowest - use min()
print(min(st))

#to find the maximum - use max()
print(max(st))

#to find the length - use len()
print(len(st)) #even in finding length of the set the duplicates is not considered
