#arithmetic operators
num1=int(input("Enter no.1: "))
num2=int(input("Enter no.2: "))
#Accept two numbers and print the floor division result (//).
print("Floor division result is: ",num1//num2)
# Accept two numbers and print the remainder (modulus).
print("modulus result is: ",num1%num2)
# Accept a number and print its square using the ** operator.
print("the square root of no.1 is: ",num1**2)
# Accept a number and print its cube.
print("the square root of no.1 is: ",num1**3)



