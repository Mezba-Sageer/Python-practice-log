#Write a program that will ask for a number of days and then will show how many hours, minutes and seconds are in that number of days.
days=int(input("enter the no.of days: "))
hours=days*24
minutes=hours*60
seconds=minutes*60
print("The no.of hours in",days,"is",hours)
print("the no.of minutes in",days,"is",minutes)
print("the no.of seconds in",days,"is",seconds)
