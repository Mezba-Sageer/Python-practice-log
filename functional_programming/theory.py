#1.Lambda :
#lambda is an anonymous function(unknown or nameless)
#it enables to brief the code into one line
#syntax: var_name=lambda arguments:operation
#example:
sum=lambda num1,num2:num1+num2 #using lambda we can avoid using function name and keyword
#and just pass the variable used within the function and the operation to be performed in one line
print(sum(30,40)) #while calling the variable we pass the arguments as well

