#sorting of list
lst=[87,453,67,89,93,23,45,34,37,100,800,19,12,27,9,22]
lst.sort() #by default sort() gives the output of ascending order.
print(lst)

lst.sort(reverse=True) #reverse=True is passed to print a list in descending order
print(lst)
#sort function in lists of strings sorts in alphabetical order
