# Object Oriented Programming - OOPS
#3 important concepts in OOPS:
#1.Class: It is a blueprint or base model.
#2.Object: design made using class is referred to as object.(something made using the blueprint)
#3.Reference: operations performed within object is reference.
#always explain the OOPS concept with example like:
#like a house: blueprint of the house is class, house is the object and activities done within a house is reference
#tv: tv itself is the class, diff brands of tv is object and watching tv,yt and other activities is the reference

#guide to creating a class
#here 'class' - is a keyword to create class and person is the class name
#so syntax for class creation is : #class (class name)
#a function created within a class is known as method
#self -is a instance keyword
#an instance keyword creates instance variable-self(this is the instance keyword)
#an instance variable is a variable used within a method which can be used within multiple methods
#instance variable is also known as dynamic variable
#another variable used within OOPS is a static variable
#a static variable is something that remains the same for different objects.

#difference between static and instance variable :
#a static variable is created within class but an instance variable is created inside a method.
#a static variable is printed by using the class name, while to print an instance variable we use the keyword self
#syntax for printing static variable: classname.staticvariablename
